<template>
  <div v-if="!item.hidden">
    <template v-if="
      hasOneShowingChild(item.children, item) &&
      onlyOneChild &&
      (!onlyOneChild.children || onlyOneChild.noShowingChildren) &&
      !item.alwaysShow
    ">
      <app-link v-if="onlyOneChild.meta" :to="resolvePath(onlyOneChild.path)">
        <el-menu-item :index="resolvePath(onlyOneChild.path)" :class="{ 'submenu-title-noDropdown': !isNest }">
          <Icon :icon="onlyOneChild.meta.icon || (item.meta && item.meta.icon)" />
          <template #title><span>{{ onlyOneChild.meta.title }}</span></template>
        </el-menu-item>
      </app-link>
    </template>

    <el-sub-menu v-else ref="subMenu" :index="resolvePath(item.path)" teleported>
      <template #title>
        <Icon :icon="item.meta?.icon" /><span>{{ item.meta?.title }}</span>
      </template>
      <sidebar-item v-for="child in item.children" :key="child.path" :is-nest="true" :item="child"
        :base-path="resolvePath(child.path)" class="nest-menu" />
    </el-sub-menu>
  </div>
</template>

<script setup lang="ts" name="sidebaritem">
import AppLink from './Link.vue'
import { RouteConfig } from '@/router'
import { isExternal } from '@/utils/validate'
import { resolve } from 'path-browserify'
import Icon from './Icon.vue'
const props = withDefaults(
  defineProps<{
    item: RouteConfig
    isNest?: boolean
    basePath: string
  }>(),
  {
    isNest: false,
    basePath: ''
  }
)

const { item, isNest, basePath } = toRefs(props)
let onlyOneChild = ref<(RouteConfig & { noShowingChildren?: boolean }) | null>(
  null
)

const hasOneShowingChild = (
  children: RouteConfig[] = [],
  parent: RouteConfig
) => {
  const showingChildren = children.filter((item) => {
    if (item.hidden) {
      return false
    } else {
      // Temp set(will be used if only has one showing child)
      onlyOneChild.value = item
      return true
    }
  })

  // When there is only one child router, the child router is displayed by default
  if (showingChildren.length === 1) {
    return true
  }

  // Show parent if there are no child router to display
  if (showingChildren.length === 0) {
    onlyOneChild.value = { ...parent, path: '', noShowingChildren: true }
    return true
  }

  return false
}

const resolvePath = (routePath: string) => {
  if (isExternal(routePath)) {
    return routePath
  }
  if (isExternal(basePath.value)) {
    return basePath.value
  }
  return resolve(basePath.value, routePath)
}
</script>
